# Data list pertama sebagai keys
keys_list = ['minggu', 'bulan', 'tahun']

# Data list kedua sebagai values
values_list = ['7 hari', '4 minggu', '12 bulan']

# Mengonversi dua data list menjadi dictionary
result_dict = dict(zip(keys_list, values_list))

# Menampilkan hasil
print("Hasil Dictionary  :", result_dict)